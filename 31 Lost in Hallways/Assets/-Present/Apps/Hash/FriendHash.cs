using UnityEngine;
using System.Collections;

//-----------------------------------------------------------------------------------------------------------------------------------
// 친구 정보를 처리하기 위한 클래스
//-----------------------------------------------------------------------------------------------------------------------------------
public class FriendHash
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
    ArrayList      friends     = new ArrayList();

	//-------------------------------------------------------------------------------------------------------------------------------
	// 친구를 등록하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
    public void Register( string id )
    {
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        if( (id)==(null) ) return;

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        friends.Add( id );

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
    }

	//-------------------------------------------------------------------------------------------------------------------------------
	// 친구 정보를 전송하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
    public void Send( APPS_LINKAGE linkage )
    {
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        Apps.SendFriends( (linkage), AppsJson.jsonEncode(friends) );

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
    }

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

