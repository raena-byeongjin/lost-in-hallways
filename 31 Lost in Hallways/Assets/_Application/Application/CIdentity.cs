				//-------------------------------------------------------------------------------------------------------------------
				// 인물 (PERSON)
				//-------------------------------------------------------------------------------------------------------------------
				case (IDENTITY.PERSON):
					return (identity);

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

	    //---------------------------------------------------------------------------------------------------------------------------
        // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return (null);
	}

