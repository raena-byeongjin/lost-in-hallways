        // -
	    //---------------------------------------------------------------------------------------------------------------------------
		foreach( tagIdentity identity in GetList() )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (identity.type)!=(type) ) continue;

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			switch( identity.type )
			{
				//-------------------------------------------------------------------------------------------------------------------
				// 기업 (COMPANY)
				//-------------------------------------------------------------------------------------------------------------------
				case (IDENTITY.COMPANY):
					if( identity.ID()==(id) )
