using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//-----------------------------------------------------------------------------------------------------------------------------------
// -
//-----------------------------------------------------------------------------------------------------------------------------------
[System.Serializable]
public class tagSystem
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public int		StandardWidth		= (1200);
	public int		StandardHeight		= (1920);
	public float	ScreenSize			= (1f);
	public int		ScreenWidth			= (1200);
	public int		ScreenHeight		= (1920);
	public float	ScreenRate			= (0);

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public static tagSystem		This		= (null);

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public tagSystem()
	{
		(This)	= (this);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 화면 크기를 설정하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static void Init()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			int ScreenHeight = GetStandardHeight();
			if( NativeScreen.IsNavigationBar() && (NativeScreen.UnFullScreenHeight)>(0) && (NativeScreen.UnFullScreenHeight)<(Screen.height) )
			{
				(ScreenHeight) = (int)( (ScreenHeight) * Func.Divide( (Screen.height), (NativeScreen.UnFullScreenHeight) ) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(This.ScreenSize)		= Func.Divide( (ScreenHeight), (Screen.height) );
			(This.ScreenWidth)		= (int)( (Screen.width)*tagSystem.GetScreenSize() );
			(This.ScreenHeight)		= (ScreenHeight);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(This.ScreenRate)		= Func.Divide( tagSystem.GetScreenWidth(), tagSystem.GetScreenHeight() );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 기준이 되는 화면 가로 너비를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static int GetStandardWidth()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.StandardWidth);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1200);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 기준이 되는 화면 세로 높이를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static int GetStandardHeight()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.StandardHeight);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1920);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 화면 크기를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static float GetScreenSize()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.ScreenSize);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1f);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 화면 가로 너비를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static int GetScreenWidth()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.ScreenWidth);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1920);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 화면 세로 높이를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static int GetScreenHeight()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.ScreenHeight);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1200);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 화면 비율을 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public static float GetScreenRate()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (This)!=(null) )
		{
			return (This.ScreenRate);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (0f);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

