using UnityEngine;
using System.Collections;

//-----------------------------------------------------------------------------------------------------------------------------------
// RECT를 설정하기 위한 클래스
//-----------------------------------------------------------------------------------------------------------------------------------
[System.Serializable]
public class tagRect
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
    public int          Left, Top, Right, Bottom;

    public int          Width, Height;

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public tagRect()
	{
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public tagRect( int left, int top, int right, int bottom )
	{
		Set( (left), (top), (right), (bottom) );
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// RECT를 설정하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
    public tagRect Set( Vector2 vPos, int width, int height )
    {
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        Set( (int)(vPos.x-width/2), (int)(vPos.y+height/2), (int)(vPos.x+width/2), (int)(vPos.y-height/2) );

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return (this);
    }

	//-------------------------------------------------------------------------------------------------------------------------------
	// RECT를 설정하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
    public void Set( int left, int top, int right, int bottom )
    {
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        if( (left)<(right) )
        {
            (Left)      = (left);
            (Right)     = (right);
        }
        else
        {
            (Left)      = (right);
            (Right)     = (left);
        }

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        if( (top)<(bottom) )
        {
            (Top)       = (top);
            (Bottom)    = (bottom);
        }
        else
        {
            (Top)       = (bottom);
            (Bottom)    = (top);
        }

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
        (Width)         = (Right-Left);
        (Height)        = (Bottom-Top);

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
    }

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public override string ToString()
	{
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return (Left)+", "+(Top)+", "+(Right)+", "+(Bottom);

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public static int SizeOf()
	{
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return (16);

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// RECT를 설정하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public void Set( tagRect rect )
	{
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		if( (rect)==(null) ) return;

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		Set( (rect.Left), (rect.Top), (rect.Right), (rect.Bottom) );

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// RECT 정보가 존재하는지 확인하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public bool Is()
	{
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		if( (Left)!=(0) || (Top)!=(0) || (Right)!=(0) || (Bottom)!=(0) )
		{
			return true;
		}

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// RECT 정보가 일치하는지 확인하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public bool IsEquals( tagRect rect )
	{
	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		if( (rect)==(null) ) return false;

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		if( (this.Left)!=(rect.Left) ) return false;
		if( (this.Top)!=(rect.Top) ) return false;
		if( (this.Right)!=(rect.Right) ) return false;
		if( (this.Bottom)!=(rect.Bottom) ) return false;

	    //---------------------------------------------------------------------------------------------------------------------------
	    // -
	    //---------------------------------------------------------------------------------------------------------------------------
		return true;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

