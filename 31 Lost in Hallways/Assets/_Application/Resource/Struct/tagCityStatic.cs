using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//-----------------------------------------------------------------------------------------------------------------------------------
// 도시 정보를 처리하기 위한 구조체
//-----------------------------------------------------------------------------------------------------------------------------------
[System.Serializable]
public class tagCityStatic : tagAppsItem
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public object				nation			= (null);
	public bool					openmarket		= (false);
	public int					extent			= (0);			//면적 (㎢)
	public int					population		= (0);			//인구
	public int					gdp				= (0);			//GDP (1인당 달러)
	public int					inflation		= (0);			//물가 (인플레이션)
	
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public uiWorldMapCityPin	pin				= (null);

	//-------------------------------------------------------------------------------------------------------------------------------
	// 국가 ID를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public tagNationStatic Nation()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (nation as tagNationStatic);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 국가 ID를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public string GetNationId()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (nation)!=(null) )
		{
			if( nation.GetType()==typeof(tagNationStatic) )
			{
				return (nation as tagNationStatic).id;
			}
			else
			if( nation.GetType()==typeof(string) )
			{
				return (nation as string);
			}
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (null);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 시장 등급을 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public int GetMarketLevel()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		float fLevel = Func.Dividef( (population), (extent) ) / (1000);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return Mathf.RoundToInt(fLevel) + (1);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 구매력을 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public int GetPurchaseLevel()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		float fLevel = (gdp) / (10000);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return Mathf.RoundToInt(fLevel) + (1);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

