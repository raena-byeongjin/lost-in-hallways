using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System.Xml;

//통화 정보를 처리하기 위한 클래스
public class CCurrencyStatic : AppsItemListener
{
	private List<tagCurrencyStatic>	Currencies	= new List<tagCurrencyStatic>();

	protected override void Awake()
	{
		base.Awake();
		Initialize( "Currency", UPDATE_CLASS.Currency.ToString() );
	}

	/*
	//-------------------------------------------------------------------------------------------------------------------------------
    // 도시 정보를 설정하기 위한 함수
