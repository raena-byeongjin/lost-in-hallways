			// -
			//-----------------------------------------------------------------------------------------------------------------------
			int time = (int)( (play.serverTime) - (Get().process) );
			SetProcessTime( time );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (play.Day)-(time)<(0) && (data.allowUpdate) )
			{
				Indigator( app.Language.Get(TEXT.사업체_정보를_불러오는중입니다) );
