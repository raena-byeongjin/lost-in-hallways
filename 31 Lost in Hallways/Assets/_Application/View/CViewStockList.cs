	//-------------------------------------------------------------------------------------------------------------------------------
	// 기업 정보를 검색하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	uiIdentity Find( tagIdentity identity )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (identity)==(null) ) return (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		foreach( uiIdentity icon in GetList() )
		{
			if( icon.Get()!=(null) && (icon.Get().type)==(identity.type) && icon.Get().ID()==identity.ID() )
			{
				return (icon);
			}
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (null);
	}

