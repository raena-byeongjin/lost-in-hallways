using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//-----------------------------------------------------------------------------------------------------------------------------------
// 앱 이용 계약 정보를 처리하기 위한 클래스
//-----------------------------------------------------------------------------------------------------------------------------------
public class CViewApplicationContract : ViewPanel
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	[System.Serializable]
	public class tagData : _tagData
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public objControl		PrivacyPolicy			= (null);
		public objControl		TermsOfUse				= (null);
		public objControl		PrivacyPolicy_View		= (null);
		public objControl		TermsOfUse_View			= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public objControl		agree					= (null);
		public objControl		reject					= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	};
	public tagData		data		= new tagData();

	//-------------------------------------------------------------------------------------------------------------------------------
	// 인터페이스를 활성화 하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override bool ON()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( base.ON(data) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
//			ForeBack();

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return base.ON();
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 인터페이스를 비활성화 하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override bool OFF()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Is() )
		{
			/*
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( Library.Is(app.Player.Name()) )
			{
				app.ViewBack.OFF( this );
			}
			*/

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			return base.OFF();
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 취소 입력에 반응하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override bool ONCANCEL( CANCEL Cancel )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Is() )
		{
			return base.ONCANCEL( Cancel );
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 인터페이스를 활성화 하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override bool ONCONTROL( objControl control, CONTROL_ACTION Action, tagFocus focus )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (control)==(null) ) return false;

		//---------------------------------------------------------------------------------------------------------------------------
		// 개인 정보 정책 (Privacy Policy)
		//---------------------------------------------------------------------------------------------------------------------------
		if( ( (control)==(data.PrivacyPolicy) || (control)==(data.PrivacyPolicy_View) ) && (Action)==(CONTROL_ACTION.SELECT) )
		{
			//app.ViewBrowser.ON( "http://naver.com" );
			Application.OpenURL(SYSTEM.PRIVACY_POLICY);
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// 이용 약관 (Terms of Use)
		//---------------------------------------------------------------------------------------------------------------------------
		if( ( (control)==(data.TermsOfUse) || (control)==(data.TermsOfUse_View) ) && (Action)==(CONTROL_ACTION.SELECT) )
		{
//			app.ViewBrowser.ON( "http://naver.com" );
			Application.OpenURL(SYSTEM.TERMS_OF_USE);
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// 동의 (AGREE)
		//---------------------------------------------------------------------------------------------------------------------------
		if( (control)==(data.agree) && (Action)==(CONTROL_ACTION.SELECT) )
		{
			app.ViewIndigator.ON(this);
//			LoveSignalHash.ApplicationContractAgree();
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// 거절 (REJECT)
		//---------------------------------------------------------------------------------------------------------------------------
		if( (control)==(data.reject) && (Action)==(CONTROL_ACTION.SELECT) )
		{
//			OFF();
			app.Confirm.ON( (TEXT.이용_계약을_거절_했습니다), (app.Engine.funcQuit) );
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 메모리를 초기화 하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override void Unloader()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.PrivacyPolicy)		= (null);
		(data.TermsOfUse)			= (null);
		(data.PrivacyPolicy_View)	= (null);
		(data.TermsOfUse_View)		= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.agree)				= (null);
		(data.reject)				= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 인터페이스를 불러오기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public override void Loader()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		base.Loader( Resources.Load("ApplicationLoader/View/ViewApplicationContract"), (play.uiCanvas) );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.PrivacyPolicy)		= (Func.Get( Transform(), "Contract/Privacy Policy" ).GetComponent(typeof(objControl)) as objControl);
		(data.TermsOfUse)			= (Func.Get( Transform(), "Contract/Terms of Use" ).GetComponent(typeof(objControl)) as objControl);
		(data.PrivacyPolicy_View)	= (Func.Get( Transform(), "Contract/Privacy Policy/View" ).GetComponent(typeof(objControl)) as objControl);
		(data.TermsOfUse_View)		= (Func.Get( Transform(), "Contract/Terms of Use/View" ).GetComponent(typeof(objControl)) as objControl);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.agree)	= (Func.Get( Transform(), "Agree" ).GetComponent(typeof(objControl)) as objControl);
		(data.reject)	= (Func.Get( Transform(), "Reject" ).GetComponent(typeof(objControl)) as objControl);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

