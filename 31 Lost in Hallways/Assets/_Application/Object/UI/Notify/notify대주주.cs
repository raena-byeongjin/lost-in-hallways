using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//-----------------------------------------------------------------------------------------------------------------------------------
// -
//-----------------------------------------------------------------------------------------------------------------------------------
public class notify대주주 : notifyCompany
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public override void Set( tagNotify notify )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (notify)==(null) ) return;

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		string name = "(NOTHING)";
		switch( CIdentity.Parse(notify.param1) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// 경영자 (PERSON)
			//-----------------------------------------------------------------------------------------------------------------------
			case (IDENTITY.PERSON):
				(name) = CApp.This.Person.GetName(CApp.This.Person.Find(notify.param2));
				break;

			//-----------------------------------------------------------------------------------------------------------------------
			// 기업 (COMPANY)
			//-----------------------------------------------------------------------------------------------------------------------
			case (IDENTITY.COMPANY):
				(name) = CApp.This.Company.GetName(CApp.This.Company.Find(notify.param2));
				break;

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(Text().text)	= Func.Script( CApp.This.Language.Get(NOTIFY.대주주), (name), CApp.This.Company.GetName(notify.from.Company()) );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		base.Set( notify );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
}

