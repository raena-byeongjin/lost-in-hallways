				(hold._RectTransform().localPosition)	= new Vector3();
				(hold._RectTransform().offsetMin)		= new Vector2( (0), (0) );
				(hold._RectTransform().offsetMax)		= new Vector2( (0), (0) );
				(icon.data.control) = (hold);
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( icon.Excute()!=(null) )
			{
				icon.Excute().OFF();
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}
		
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		icon.SetRaycastTarget( false );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

