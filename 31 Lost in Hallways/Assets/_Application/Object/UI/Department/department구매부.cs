		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			icon.Alpha( 0.15f );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			objControl hold = (Func.Create( icon.Transform(), (Resources.Load(PATH.UI_LIST_ITEM_HOLD) as GameObject) ).GetComponent(typeof(objControl)) as objControl);
			if( hold._RectTransform()!=(null) )
