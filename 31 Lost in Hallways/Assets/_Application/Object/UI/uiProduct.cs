		//---------------------------------------------------------------------------------------------------------------------------
		if( Company()!=(null) && Company().Image()!=(null) )
		{
			(Company().Image().raycastTarget) = (option);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Text()!=(null) )
		{
			(Text().raycastTarget) = (option);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Nation()!=(null) && Nation().Image()!=(null) )
		{
			(Nation().Image().raycastTarget) = (option);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( City()!=(null) )
		{
			if( City().Image()!=(null) )	(City().Image().raycastTarget)	= (option);
			if( City().Name()!=(null) )		(City().Name().raycastTarget)	= (option);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Graph()!=(null) && Graph().Image()!=(null) )
		{
			(Graph().Image().raycastTarget) = (option);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

