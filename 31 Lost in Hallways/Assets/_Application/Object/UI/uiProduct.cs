	//-------------------------------------------------------------------------------------------------------------------------------
	// 아이템 정보를 설정 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public void Set( tagProductStatic productstatic, AppsParameter col=(null) )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (productstatic)==(null) ) return;
//		if( (col)==(null) ) return;					//(NULL)값을 허용함

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.productstatic)	= (productstatic);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( true )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			Set( CApp.This.Language.Get(Get().Name()) );
			Set( Get().GetTexture() );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( productstatic.CorverTexture.Is() && !productstatic.CorverTexture.IsLoad() )
			{
				CApp.This.Download.Indigator();
				CApp.This.ProductStatic.CorverTextureDownload( (productstatic), (funcSet), (productstatic) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (col)!=(null) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.id)	= col.Get("id");
			if( Business()!=(null) )
			{
				(Business().data.business)	= col.Get("business");	//사업체
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// 품질, 브랜드
			//-----------------------------------------------------------------------------------------------------------------------
			int nQuality	= (int)col.GetFloat("quality");
			int nBrand		= (int)( col.GetFloat("brand_recognition") + col.GetFloat("brand_reliability") );

			//-----------------------------------------------------------------------------------------------------------------------
			// 원가, 수량, 품질
			//-----------------------------------------------------------------------------------------------------------------------
			if( Cost()!=(null) )	(Cost().text)		= CApp.This.Cash.ExchangeToString( (float)col.GetFloat("cost") );
			if( Count()!=(null) )	(Count().text)		= Func.NumberUnit( col.GetInt("count") );
			if( Quality()!=(null) )	(Quality().text)	= nQuality.ToString();
			if( Brand()!=(null) )	(Brand().text)		= nBrand.ToString();

			//-----------------------------------------------------------------------------------------------------------------------
			// 품질
			//-----------------------------------------------------------------------------------------------------------------------
			if( (data.qualityValue)!=(null) )
			{
				data.qualityValue.ON( Func.Divide( (nQuality), (100f) ) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// 브랜드
			//-----------------------------------------------------------------------------------------------------------------------
			if( (data.brandValue)!=(null) )
			{
				data.brandValue.ON( Func.Divide( (nBrand), (100f) ) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// 가격
			//-----------------------------------------------------------------------------------------------------------------------
			if( Price()!=(null) )
			{
				if( col.GetFloat("price")>(0) )
				{
					(Price().text)	= CApp.This.Cash.ExchangeToString( col.GetFloat("price") );
				}
				else
				{
					(Price().text)	= "-";
				}
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( Demand()!=(null) || Supply()!=(null) )
			{
				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				int	demand				= col.GetInt("demand");
				int	supply				= col.GetInt("supply");
				int	DemandAndSupplyMax	= Mathf.Max( (demand), (supply) );

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				if( Demand()!=(null) )	(Demand()._RectTransform().sizeDelta)	= new Vector2( (Demand()._RectTransform().sizeDelta.x), Mathf.Max( (5), Func.Divide( (256), (DemandAndSupplyMax) ) * (demand) ) );
				if( Supply()!=(null) )	(Supply()._RectTransform().sizeDelta)	= new Vector2( (Supply()._RectTransform().sizeDelta.x), Mathf.Max( (5), Func.Divide( (256), (DemandAndSupplyMax) ) * (supply) ) );

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
			}

#if UNITY_EDITOR
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.nDemand)	= col.GetInt("demand");
			(data.nSupply)	= col.GetInt("supply");
#endif

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

