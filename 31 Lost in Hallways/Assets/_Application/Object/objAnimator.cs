using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//-----------------------------------------------------------------------------------------------------------------------------------
// 애니메이터 정보를 처리하기 위한 클래스
//-----------------------------------------------------------------------------------------------------------------------------------
public class objAnimator : MonoBehaviour
{
	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	[System.Serializable]
	public class tagData
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public Animator								animator			= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public int									layerIndex			= (-1);
		public string								stateName			= (null);
		public float								playNormalize		= (0);
		public string								playName			= (null);
		public ANIMATION_FRAME						result				= (ANIMATION_FRAME.NOTHING);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public float								normalize			= (0f);
		public bool									loop				= (false);
		public bool									hit					= (false);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		public List<tagAnimatorLayerProcess>		LayerProcesses		= new List<tagAnimatorLayerProcess>();

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	};
	public tagData		data		= new tagData();

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	void Awake()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.animator)		= (GetComponentInChildren(typeof(Animator)) as Animator);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(enabled)	= (false);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	void Update()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		tagAnimatorLayerProcess		layerProcess	= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// 애니메이션 레이어의 페이드-인, 아웃을 처리함
		//---------------------------------------------------------------------------------------------------------------------------
		for( int i=0; i<(data.LayerProcesses.Count); i++ )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(layerProcess)	= (data.LayerProcesses[i]);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( !LayerProcess(layerProcess) )
			{
				data.LayerProcesses.RemoveAt(i);
				(i)	-= (1);
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// 기본 레이어의 재생을 처리함
		//---------------------------------------------------------------------------------------------------------------------------
		if( Library.Is(data.stateName) && Animator()!=(null) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( Animator()!=(null) )
			{
				Animator().Play( (data.stateName), (0), (data.playNormalize) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.playName)		= (data.stateName);
			SetNormalize( data.playNormalize );
			(data.stateName)	= (null);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(enabled)			= (false);

			//-----------------------------------------------------------------------------------------------------------------------
			// 기존 재생중이던 레이어는 중단함
			//-----------------------------------------------------------------------------------------------------------------------
			for( int i=(1); i<(Animator().layerCount); i++ )
			{
				Animator().SetLayerWeight( (i), (0f) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			Animator().SetLayerWeight( (0), (1f) );
			data.LayerProcesses.Clear();

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.layerIndex)	= (0);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			RawAnimatorEnable( true );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}
		else
		if( (data.LayerProcesses.Count)<=(0) )
		{
			(enabled)	= (false);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 애니메이션을 재생하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public bool Play( string stateName, float normalize=(0f), float fade=(0.25f) )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (stateName)==(null) ) return false;
		if( Animator()==(null) ) return false;

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		int		layerIndex		= Animator().GetLayerIndex(stateName);
		if( (layerIndex)>=(0) && ( (data.layerIndex)!=(layerIndex) || !IsPlay( (layerIndex), (ANIMATION.PLAY) ) ) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (data.layerIndex)!=(layerIndex) )
			{
				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				foreach( tagAnimatorLayerProcess layerprocess in data.LayerProcesses )
				{
					if( (layerprocess.Action)==(OBJECT_ACTION.FADE_IN) )
					{
						(layerprocess.Action)	= (OBJECT_ACTION.FADE_OUT);
					}
				}

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				if( (fade)<=(0) || (data.layerIndex)<(0) )
				{
					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					if( (data.layerIndex)>=(0) )
					{
						Animator().SetLayerWeight( (data.layerIndex), (0) );
						Animator().Play( ANIMATION.DEFAULT );
					}

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					Animator().SetLayerWeight( (layerIndex), (1) );

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
				}
				else
				{
					SetLayerProcess( (data.layerIndex), (OBJECT_ACTION.FADE_OUT), (fade) );
					SetLayerProcess( (layerIndex), (OBJECT_ACTION.FADE_IN), (fade) );
				}

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (layerIndex)==(0) )
			{
				Animator().Play( (ANIMATION.DEFAULT), (layerIndex), (normalize) );
			}
			else
			{
				Animator().Play( (ANIMATION.PLAY), (layerIndex), (normalize) );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			RawAnimatorEnable( true );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.stateName)	= (null);
			(data.playName)		= (stateName);
			(data.layerIndex)	= (layerIndex);
			SetHit( false );
			SetNormalize( Normalize() );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (data.layerIndex)!=(0) )
			{
				SetLoop( IsRawLoop() );
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (fade)>(0) )
			{
				//동일 레이어에서의 애니메이션 변경이어도 프레임 상태를 확인하기 위해
				//페이드 작업은 항상 시작함
				(enabled)	= (true);
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (layerIndex)<(0) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(data.stateName)		= (stateName);
			SetNormalize( normalize );
			(data.playNormalize)	= (normalize);
			SetHit( false );
			SetLoop( false );

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			(enabled)	= (true);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 애니메이션 프레임을 처리하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public ANIMATION_FRAME Frame()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( !IsRawEnable() || ( Result()==(ANIMATION_FRAME.END) && (data.LayerProcesses.Count)<=(0) ) )
		{
			return Result(ANIMATION_FRAME.NOTHING);
		}
		else
		if( !Library.Is(data.stateName) )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			if( (data.normalize)>GetRawNormalize() )
			{
				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				if( IsLoop() )
				{
					SetNormalize( Normalize() );
					return Result(ANIMATION_FRAME.REPEAT);
				}

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
				if( (enabled) || (data.LayerProcesses.Count)>(0) )
				{
					//애니메이션이 아직 페이드 작업 중이기 때문에, OVER를 반환하기 위해 END는 통과시킴
				}
				else
				{
					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					//Stop();	//페이드 효과로 반복되는 애니메이션이 중단되기 때문에, Stop하지 않음

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					return Result(ANIMATION_FRAME.END);
				}

				//-------------------------------------------------------------------------------------------------------------------
				// -
				//-------------------------------------------------------------------------------------------------------------------
			}

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		SetNormalize( GetRawNormalize() );

		/*
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( Result()!=(ANIMATION_FRAME.OVER) )
		{
			SetLoop( IsRawLoop() );
		}
		*/

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( !(data.hit) && (data.normalize)>=(0.5f) )
		{
			SetHit( true );
			return Result(ANIMATION_FRAME.HIT);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return Result(ANIMATION_FRAME.OVER);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 프레임 결과를 설정하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public ANIMATION_FRAME Result( ANIMATION_FRAME nFrame )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (nFrame)==(ANIMATION_FRAME.NOTHING) || (nFrame)==(ANIMATION_FRAME.REPEAT) || (nFrame)==(ANIMATION_FRAME.END) )
		{
			SetHit( false );
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(data.result)	= (nFrame);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (nFrame);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 프레임 결과를 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public ANIMATION_FRAME Result()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (data.result);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	bool LayerProcess( tagAnimatorLayerProcess layerProcess )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (layerProcess)==(null) ) return false;

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		switch( layerProcess.Action )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// 페이드-인
			//-----------------------------------------------------------------------------------------------------------------------
			case (OBJECT_ACTION.FADE_IN):
				{
					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					float	weight	= Animator().GetLayerWeight(layerProcess.layerIndex);
					(weight)	+= (1f/layerProcess.time) * GetDeltaTime();

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					Animator().SetLayerWeight( (layerProcess.layerIndex), (weight) );
					if( (weight)>=(1) )
					{
						return false;
					}

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
				}
				break;

			//-----------------------------------------------------------------------------------------------------------------------
			// 페이드-아웃
			//-----------------------------------------------------------------------------------------------------------------------
			case (OBJECT_ACTION.FADE_OUT):
				{
					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					float	weight	= Animator().GetLayerWeight(layerProcess.layerIndex);
					(weight)	-= (1f/layerProcess.time) * GetDeltaTime();

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
					Animator().SetLayerWeight( (layerProcess.layerIndex), (weight) );
					if( (weight)<=(0) )
					{
						Animator().Play( ANIMATION.DEFAULT );
						return false;
					}

					//---------------------------------------------------------------------------------------------------------------
					// -
					//---------------------------------------------------------------------------------------------------------------
				}
				break;

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return true;
	}

