	//-------------------------------------------------------------------------------------------------------------------------------
	// 애니메이션이 재생 중인지 확인하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public bool IsRawEnable()
	{
        //---------------------------------------------------------------------------------------------------------------------------
        // -
        //---------------------------------------------------------------------------------------------------------------------------
		if( Result()==(ANIMATION_FRAME.END) )
		{
			//마지막 프레임은 재생 중인것으로 결과를 리턴함
			return true;
		}
		else
		if( Animator()!=(null) )
		{
			return (Animator().isActiveAndEnabled);
		}

        //---------------------------------------------------------------------------------------------------------------------------
        // -
        //---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 재생중인 애니메이션을 확인하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public bool IsPlay()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (data.layerIndex)<(0) )
		{
			//애니메이션이 아직 재생되지 않음
			return false;
		}
		else
		if( Result()==(ANIMATION_FRAME.END) )
		{
			//마지막 프레임은 재생 중인것으로 결과를 리턴함
			return true;
		}
		else
		if( !IsRawEnable() )
		{
			//애니메이션의 재생이 중지되어 있음
			return false;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		AnimatorStateInfo	aniState	= Animator().GetCurrentAnimatorStateInfo(data.layerIndex);
		if( (data.LayerProcesses.Count)>(0) )
		{
			//페이드를 처리 중
			return true;
		}
		else
		if( (aniState.length)<=(0) )
		{
			//애니메이션 클립이 설정되어 있지 않음
			return false;
		}
		else
		if( (data.layerIndex)>(0) && !aniState.IsName(ANIMATION.PLAY) )
		{
			//추가 레이어의 재생이 종료됨
			return false;
		}
		else
		if( (data.layerIndex)==(0) && !aniState.IsName(data.playName) )
		{
			//기본 레이어의 재생이 종료됨
			return false;
		}
		else
		if( IsLoop() || (aniState.normalizedTime)<(1) )
		{
			return true;
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return false;
	}

