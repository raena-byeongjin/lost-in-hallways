	//-------------------------------------------------------------------------------------------------------------------------------
	// 정규화된 재생 시간을 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public float GetRawNormalize()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (data.stateName)!=(null) )
		{
			//재생이 대기 중이므로, 예약된 재생 시간을 리턴함
			return (data.playNormalize);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return GetNormalizeTime()%(1);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 정규화된 재생 시간을 얻기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public float GetNormalizeTime()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( IsPlay() )
		{
			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			AnimatorStateInfo	stateInfo	= Animator().GetCurrentAnimatorStateInfo(data.layerIndex);

			//-----------------------------------------------------------------------------------------------------------------------
			// -
			//-----------------------------------------------------------------------------------------------------------------------
			return (stateInfo.normalizedTime);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (0);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 애니메이션을 재생하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public void SetLayerProcess( int layerIndex, OBJECT_ACTION Action, float time )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (layerIndex)<(0) || (layerIndex)>=(Animator().layerCount) ) return;

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		tagAnimatorLayerProcess		layerProcess	= FindLayerProcess(layerIndex);
		if( (layerProcess)!=(null) )
		{
			data.LayerProcesses.Remove(layerProcess);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(layerProcess)	= new tagAnimatorLayerProcess();

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(layerProcess.layerIndex)	= (layerIndex);
		(layerProcess.Action)		= (Action);
		(layerProcess.time)			= (time);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		data.LayerProcesses.Add( layerProcess );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// 애니메이션을 재생하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public tagAnimatorLayerProcess FindLayerProcess( int layerIndex )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (layerIndex)<(0) || (layerIndex)>=(Animator().layerCount) ) return (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		foreach( tagAnimatorLayerProcess layerProcess in data.LayerProcesses )
		{
			if( (layerProcess.layerIndex)==(layerIndex) )
			{
				return (layerProcess);
			}
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (null);
	}

